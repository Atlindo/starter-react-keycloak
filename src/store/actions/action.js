export const SUCCESS = (type) => {
  return [type,'Success'].join('')
}
export const FAILURE = (type) => {
  return [type,'Failure'].join('')
}
export const REQUEST = (type) => {
  return [type,'Request'].join('')
}
export const NOTFOUND = (type) => {
  return [type,'NotFound'].join('')
}
export const EVENT = (type) => {
  return [type,'event'].join('')
}
